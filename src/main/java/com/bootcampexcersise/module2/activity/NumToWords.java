package com.bootcampexcersise.module2.activity;

public class NumToWords {
    public static void main(String[] args) {
        int x = 5;
        switch (x){
            case 1:
                System.out.println("Value of x is not 1");
                break;
            case 2:
                System.out.println("Value of x is not 2");
                break;
            case 3:
            System.out.println("Value of x is not 3");
            break;
            case 4:
                System.out.println("Value of x is not 4");
                break;
            case 5:
                System.out.println("Value of x is 5");
            break;
            case 6:
                System.out.println("Value of x is not 6");
                break;
            case 7:
                System.out.println("Value of x is not 7");
            break;
            case 8:
                System.out.println("Value of x is not 8");
                break;
            case 9:
                System.out.println("Value of x is not 9");
                break;
            case 10:
                System.out.println("Value of x is not 10");
                break;
            default:
                System.out.println("Value of x is null");
        }
    }
}
